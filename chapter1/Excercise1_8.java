package chapter1;

public class Excercise1_8 {
	public static void main(String[] args) {
		final double Pi = 3.14;
		double radius = 5.5;
		double perimeter = 2 * radius * Pi;
		double area = radius * radius * Pi;
				
		System.out.println("The perimeter of a circle that has a " +
				"radius of 5.5 is " + perimeter);
		System.out.println("The area of a circle that has a " +
				"radius of 5.5 is " + area);
		
	}
}
